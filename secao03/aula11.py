# Operadores de comparações (relacionais)
# OP ----------- Significado ---------- Exemplo (True)
# > ------------- maior ---------------- 2 > 1
# >= ------------ maior ou igual ------- 2 >= 2
# < ------------- menor ---------------- 1 < 2
# <= ------------ menor ou igual ------- 2 <= 2
# == ------------ igual --------------- 'a' == 'a'
# != ------------ diferente ----------- 'a' != 'b'

maior = 2 > 1
meior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 10 == 10
diferente = 11 != 'b'
print(diferente)